package leetcode.editor.cn;

import java.util.Comparator;
import java.util.PriorityQueue;

//leetcode submit region begin(Prohibit modification and deletion)
class MedianFinder {

    PriorityQueue<Integer> small ;//å°æ ¹å †
    PriorityQueue<Integer> big ;//å¤§æ ¹å †

    /** initialize your data structure here. */
    public MedianFinder() {
        small = new PriorityQueue<Integer>();
        big = new PriorityQueue<Integer>(new Comparator<Integer>(){
            public int compare(Integer t1, Integer t2){
                return t2-t1;
            }
        }) ;
    }
    
    public void addNum(int num) {
        if (big.size() == 0){
            big.offer(num);
            return;
        }

        if (big.size() == small.size()){
            if (num <= small.peek()){
                big.offer(num);
            }else{
                big.offer(small.poll());
                small.offer(num);
            }
        }else{
            if (num >= big.peek()){
                small.offer(num);
            }else{
                small.offer(big.poll());
                big.offer(num);
            }
        }
    }
    
    public double findMedian() {
        if (big.size() == small.size()){
            return ((double)big.peek()+small.peek())/2;
        }else{
            return (double)big.peek();
        }
    }
}

