class Solution {
    public boolean canFinish(int numCourses, int[][] prerequisites) {

        //èŠ‚ç‚¹å…¥åº¦
        int[] k = new int[numCourses];

        //è¾¹
        ArrayList<Integer>[] edge = new ArrayList[numCourses];

        boolean[] flag = new boolean[numCourses];

        for (int i = 0; i < prerequisites.length; i++) {
            int[] temp = prerequisites[i];
            int left = temp[0];
            int right = temp[1];

            //æ›´æ–°å…¥åº¦
            k[left]++;

            //æ›´æ–°è¾¹
            if (flag[right] == false){
                ArrayList<Integer> e = new ArrayList<>();
                e.add(left);
                edge[right] = e;
                flag[right] = true;
            }else{
                edge[right].add(left);
            }
        }

        //è®°å½•å…¥åº¦ä¸º0çš„èŠ‚ç‚¹æ•°
        int nums = 0;

        //å­˜æ”¾å…¥åº¦ä¸º0çš„èŠ‚ç‚¹
        Stack<Integer> stack = new Stack<>();
        for (int i = 0; i < numCourses; i++) {
            if (k[i] == 0){
                stack.add(i);
                nums++;
            }
        }


        while (!stack.isEmpty()){
            Integer pop = stack.pop();
            if (flag[pop] == true){
                ArrayList<Integer> list = edge[pop];
                for (int temp : list){
                    k[temp]--;
                    if (k[temp] == 0){
                        stack.add(temp);
                        nums++;
                    }
                }
            }
        }

        return nums == numCourses;
    }
}
