class Solution {
    public String addBinary(String a, String b) {
        StringBuilder sb = new StringBuilder();
        int n = a.length()-1, m = b.length()-1, plus = 0;
        while (n>=0||m>=0){
            int sum = plus;
            if (n>=0){
                sum += a.charAt(n--)-'0';
            }
            if (m>=0){
                sum += b.charAt(m--)-'0';
            }
            sb.append(sum&1);
            plus = sum >> 1;
        }
        if (plus == 1){
            sb.append(1);
        }
        return sb.reverse().toString();
    }
}
