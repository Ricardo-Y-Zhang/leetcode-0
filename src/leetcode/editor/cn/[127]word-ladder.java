class Solution {
    public int ladderLength(String beginWord, String endWord, List<String> wordList) {
        if (!wordList.contains(endWord)) {
            return 0;
        }
        wordList.add(beginWord);
        int n = wordList.size();
        boolean[][] isSimilar = new boolean[n][n];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                boolean flag = judge(wordList.get(i), wordList.get(j));
                isSimilar[i][j] = flag;
                isSimilar[j][i] = flag;
            }
        }
        Queue<String> queue = new LinkedList<>();
        queue.add(beginWord);
        HashSet<String> set = new HashSet<>();
        set.add(beginWord);
        int count = 1;
        while (!queue.isEmpty()) {
            int size = queue.size();
            count++;
            for (int i = 0; i < size; i++) {
                String first = queue.poll();
                int index = wordList.indexOf(first);
                for (int j = 0; j < n; j++) {
                    if (isSimilar[index][j] && !set.contains(wordList.get(j))){
                        queue.add(wordList.get(j));
                        set.add(wordList.get(j));
                        if (wordList.get(j).equals(endWord)){
                            return count;
                        }
                    }
                }
            }
        }
        return 0;
    }
    public boolean judge(String str1, String str2) {
        boolean flag = false;
        for (int i = 0; i < str1.length(); i++) {
            if (str1.charAt(i) != str2.charAt(i)) {
                if (!flag) {
                    flag = true;
                }else{
                    return false;
                }
            }
        }
        return true;
    }
}
