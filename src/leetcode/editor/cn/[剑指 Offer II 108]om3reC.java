package leetcode.editor.cn;

import java.util.HashSet;
import java.util.LinkedList;
import java.util.List;
import java.util.Queue;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
    public int ladderLength(String beginWord, String endWord, List<String> wordList) {
        if (!wordList.contains(endWord)){
            return 0;
        }
        int n = wordList.size();
        wordList.add(beginWord);
        wordList.add(endWord);
        boolean[][] matrix  = new boolean[n+2][n+2];
        for (int i = 0; i < n+2; i++) {
            for (int j = i+1; j < n+2; j++) {
                boolean flag = convert(wordList.get(i),wordList.get(j));
                matrix[i][j] = flag;
                matrix[j][i] = flag;
            }
        }
        Queue<String> queue = new LinkedList<>();
        queue.add(beginWord);
        int step = 1;
        HashSet<String> set = new HashSet<>();
        set.add(beginWord);
        while (!queue.isEmpty()){
            int size = queue.size();
            for (int i = 0; i < size; i++) {
                String first = queue.poll();
                if (first.equals(endWord)){
                    return step;
                }
                int index = wordList.indexOf(first);
                for (int j = 0; j < n+2; j++) {
                    String next = wordList.get(j);
                    if (!set.contains(next) && matrix[index][j] == true){
                        queue.add(next);
                        set.add(next);
                    }
                }
            }
            step++;
        }
        return 0;
    }

    public boolean convert(String str1, String str2){
        int diff = 0;
        for (int i = 0; i < str1.length(); i++) {
            if (str1.charAt(i) != str2.charAt(i)){
                if (diff != 0){
                    return false;
                }
                diff++;
            }
        }
        return true;
    }
}
